package entidades;

public class RolOpciones 
{
	//Atributos
	private int rolOpciones;
	private int rol;
	private int opcion;
	
	//Métodos
	public int getRolOpciones() {
		return rolOpciones;
	}
	public void setRolOpciones(int rolOpciones) {
		this.rolOpciones = rolOpciones;
	}
	public int getRol() {
		return rol;
	}
	public void setRol(int rol) {
		this.rol = rol;
	}
	public int getOpcion() {
		return opcion;
	}
	public void setOpcion(int opcion) {
		this.opcion = opcion;
	}
	
	
}
